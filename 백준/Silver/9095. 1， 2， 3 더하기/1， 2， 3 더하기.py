# 1, 2, 3의 합으로 나타내는 방법의 수
# 1: 1 / 1가지
# 2: 1+1, 2 / 2가지
# 3: 1+1+1, 2+1, 1+2, 3 / 4가지
# 4: 1+1+1+1, 2+1+1, 1+2+1, 3+1, 1+1+2, 2+2, 1+3 / 7가지
# f(n) = f(n-1) + f(n-2) + f(n-3)
dp = [0]*(11)
dp[1] = 1
dp[2] = 2
dp[3] = 4
for i in range(4, 11):
    dp[i] = dp[i-1] + dp[i-2] + dp[i-3]

T = int(input())
for _ in range(T):
    n = int(input())
    print(dp[n])